public class DataTypes {
    public static void main(String[] args) {
        System.out.println("=== Java Data Types ===\n");

        int intA = 10;
        long longNum = 123456789L;
        double doubleNum = 3.14159;
        float floatNum = 3.14f;
        char grade = 'A';
        boolean isJavaFun = true;
        String greeting = "Hello, Java!";

        System.out.println("int: " + intA);
        System.out.println("long: " + longNum);
        System.out.println("double: " + doubleNum);
        System.out.println("float: " + floatNum);
        System.out.println("char: " + grade);
        System.out.println("boolean: " + isJavaFun);
        System.out.println("String: " + greeting);

        System.out.println("\n=== C++ Comparison ===");
        System.out.println("C++: int a = 10; double b = 3.14;");
        System.out.println("Java: int a = 10; double b = 3.14; (almost the same!)");
    }
}